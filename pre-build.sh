#!/bin/bash

ZAPRET_VERSION="72.9"
NFQWS_DIR="padavan-ng/trunk/user/nfqws"
echo "Cleaning old zapret files..."
rm -rf "$NFQWS_DIR"/zapret-* "$NFQWS_DIR"/zapretsh-main
rm -f "$NFQWS_DIR"/*.tar.gz
echo "✓ Old zapret files cleaned"
CURRENT_VERSION=$(grep -E '^SRC_VER\s*=\s*[0-9.]+' "$NFQWS_DIR/Makefile" | head -1 | awk -F'=' '{print $2}' | tr -d ' ')
[[ -z "$CURRENT_VERSION" ]] && echo "Cannot detect zapret version" && exit 1
echo "Updating zapret from $CURRENT_VERSION to $ZAPRET_VERSION"
sed -i "s/^SRC_VER = $CURRENT_VERSION/SRC_VER = $ZAPRET_VERSION/" "$NFQWS_DIR/Makefile"
grep -q "^SRC_VER = $ZAPRET_VERSION" "$NFQWS_DIR/Makefile" && echo "✓ Zapret version updated" || (echo "Update failed" && exit 1)
echo "Done."


set -e  # Прерывать при ошибках

FILENAME="TL_EC220_G5-V3.tar.gz"
TARGET_DIR="padavan-ng/trunk/configs/boards/TPLINK"

echo "=== Распаковка $FILENAME ==="

# Проверка существования файла
if [ -f "$FILENAME" ]; then
    echo "Файл уже существует: $FILENAME"
    exit 0
fi

# Извлечение base64
echo "Извлекаю данные..."
BASE64_DATA=$(awk -v f="$FILENAME" '
    $0 == "# BEGIN_B64_ " f {start=1; next}
    $0 == "# END_B64_ " f {exit}
    start {printf "%s", $0}
' "$0")

if [ -z "$BASE64_DATA" ]; then
    echo "ОШИБКА: Не найдены base64 данные"
    exit 1
fi

# Декодирование
echo "Декодирую base64..."
echo "$BASE64_DATA" | base64 -d > "$FILENAME"

if [ $? -ne 0 ] || [ ! -f "$FILENAME" ]; then
    echo "ОШИБКА: Не удалось декодировать"
    exit 1
fi

echo "Файл создан: $FILENAME ($(stat -c%s "$FILENAME") байт)"

# Распаковка в целевую директорию
echo "Распаковываю в $TARGET_DIR..."
mkdir -p "$TARGET_DIR"

if tar -xzf "$FILENAME" -C "$TARGET_DIR"; then
    echo "УСПЕХ: Архив распакован в $TARGET_DIR"
    ls -la "$TARGET_DIR/"
else
    echo "ОШИБКА: Не удалось распаковать архив"
    exit 1
fi

echo "=== Готово ==="
exit 0

